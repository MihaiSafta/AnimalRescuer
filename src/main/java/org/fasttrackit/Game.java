package org.fasttrackit;

public class Game {
    String adopter;
    String dog;
    String vet;
    int gamenumber;

    public Game(String adopter, String dog, String vet, int gamenumber) {
        this.adopter = adopter;
        this.dog = dog;
        this.vet = vet;
        this.gamenumber = gamenumber;
    }
}
